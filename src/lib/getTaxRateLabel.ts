/**
 * getTaxRateLabel
 * - 과세표준에 따라 적용된 세율(rate)을 기반으로, 사용자에게 보여줄 설명 문구를 반환하는 함수
 * - 세율 구간(6% ~ 45%)에 따라 직관적인 설명을 이모지와 함께 제공
 * - 반환되는 문자열은 UI에서 바로 렌더링될 수 있도록 설명 형태로 구성됨
 *
 * @param rate - 적용된 세율 값 (예: 0.15 → 15%)
 * @returns 해당 세율 구간에 대한 사용자 친화적 설명 문자열
 *
 * 주의: 계산된 세율 값은 소수점 형태로 전달되어야 함 (0.06, 0.15 등)
 */

export function getTaxRateLabel(rate: number): string {
  switch (rate) {
    case 0.06:
      return "📊 귀하는 과세표준이 1,400만원 이하로, 낮은 세율 구간(6%)에 해당합니다.";
    case 0.15:
      return "📊 귀하는 과세표준이 1,400만원 초과~5,000만원 이하로, 일반 세율 구간(15%)에 해당합니다.";
    case 0.24:
      return "📊 귀하는 과세표준이 5,000만원 초과~8,800만원 이하로, 중간 세율 구간(24%)에 해당합니다.";
    case 0.35:
      return "📊 귀하는 과세표준이 8,800만원 초과~1억 5,000만원 이하로, 고소득 세율 구간(35%)에 해당합니다.";
    case 0.38:
      return "📊 귀하는 과세표준이 1억 5,000만원 초과~3억원 이하로, 고소득 세율 구간(38%)에 해당합니다.";
    case 0.4:
      return "📊 귀하는 과세표준이 3억원 초과~5억원 이하로, 고소득 세율 구간(40%)에 해당합니다.";
    case 0.42:
      return "📊 귀하는 과세표준이 5억원 초과~10억원 이하로, 고소득 세율 구간(42%)에 해당합니다.";
    case 0.45:
      return "🚨 귀하는 과세표준이 10억원을 초과하여, 최고 세율 구간(45%)에 해당합니다.";
    default:
      return "⚠️ 세율 구간 정보를 불러오지 못했습니다.";
  }
}
